//! Font metrics for the 14 standard PDF base fonts.
//!
//! Character widths sourced from the standard Adobe AFM (Adobe Font Metrics)
//! files for the base 14 fonts, as specified by ISO 32000-1:2008 §9.6.2.2
//! and Annex D. Widths are in units of 1/1000 of the text space unit.
//!
//! Standard base fonts use WinAnsiEncoding — each character code 0–255 maps to
//! exactly one glyph. Stamp text is ASCII-range (document IDs, names, dates),
//! so byte-level width indexing is correct.

/// Glyph widths for a single font face, indexed by character code (0–255).
pub(crate) struct FontMetrics {
    widths: [u16; 256],
}

impl FontMetrics {
    /// Width of a single character in 1/1000 text units.
    pub(crate) fn char_width(&self, byte: u8) -> u16 {
        self.widths[byte as usize]
    }
}

/// Look up metrics for a standard base font by name.
/// Falls back to Helvetica for unrecognized fonts.
pub(crate) fn font_metrics(base_font: &str) -> &'static FontMetrics {
    match base_font {
        "Helvetica" | "Helvetica-Oblique" => &HELVETICA,
        "Helvetica-Bold" | "Helvetica-BoldOblique" => &HELVETICA_BOLD,
        "Times-Roman" => &TIMES_ROMAN,
        "Times-Bold" => &TIMES_BOLD,
        "Times-Italic" => &TIMES_ITALIC,
        "Times-BoldItalic" => &TIMES_BOLD_ITALIC,
        "Courier" | "Courier-Bold" | "Courier-Oblique" | "Courier-BoldOblique" => &COURIER,
        "Symbol" => &SYMBOL,
        "ZapfDingbats" => &ZAPF_DINGBATS,
        _ => &HELVETICA,
    }
}

/// Measure text width in points.
///
/// Sums glyph widths for each byte of the text string at the given font and
/// size. Designed for WinAnsiEncoding (single-byte) text used by stamps.
///
/// Non-ASCII bytes (0x80+) that are UTF-8 continuation bytes (0x80..0xBF) are
/// skipped to avoid double-counting multi-byte characters. Leading bytes of
/// multi-byte sequences are measured using their WinAnsiEncoding width, which
/// gives a reasonable approximation since stamp text is predominantly ASCII.
pub(crate) fn text_width_pt(base_font: &str, text: &str, font_size: f64) -> f64 {
    let metrics = font_metrics(base_font);
    let width_units: u32 = text.bytes()
        .filter(|b| !matches!(b, 0x80..=0xBF))
        .map(|b| metrics.char_width(b) as u32)
        .sum();
    width_units as f64 * font_size / 1000.0
}

